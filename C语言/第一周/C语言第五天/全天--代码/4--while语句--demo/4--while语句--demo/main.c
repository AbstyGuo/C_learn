#include <stdio.h>

int main(int argc, const char * argv[])
{

    //打印1-100的所有正整数
    
    int i = 1;//循环变量
    while(i <= 100)
    {
        printf("%d ",i);
        i++;
    }
    
    
    
    return 0;
}









